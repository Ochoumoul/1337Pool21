int	ft_sqrt(int nb)
{
	int	i;

	if (nb > 0)
	{
		if (nb == 0 || nb == 1)
			return (nb);
		i = 2;
		while (nb / 2 >= i && i < 46341)
		{
			if (nb == (i * i))
				return (i);
			i++;
		}
	}
	return (0);
}
